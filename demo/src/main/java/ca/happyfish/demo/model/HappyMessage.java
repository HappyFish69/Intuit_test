package ca.happyfish.demo.model;

public class HappyMessage {
    long id;
	String message;
	
    public long getId() {
    	return id;
    }
    
    public void setId(long mESSAGE_ID) {
    	this.id = mESSAGE_ID;
    }

    public String getMessage() {
    	return message;
    }
    
    public void setMessage(String message) {
    	this.message = message;
    }
}
